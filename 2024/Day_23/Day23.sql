drop table if exists #Input
drop table if exists #Input1

select Comp1, Comp2
into #Input
from string_split(replace(@Input, char(10), ''), char(13)) r
	cross apply (select replace([value], '-', '.') rw) n
	cross apply (select parsename(rw, 1) Comp1, parsename(rw, 2) Comp2) c

;with i as
	(select Comp1, Comp2
		from #Input i
		union
		select Comp2, Comp1
		from #Input i
	)
select *, row_number() over(order by (select 1)) SetID
into #Input1
from i

;with g as
	(select concat(i.Comp1, ',', i.Comp2, ',', i1.Comp2) grp, row_number() over(order by (select 1)) ID
		from #Input1 i
			inner join #Input1 i1 on i1.SetID != i.SetID
								and i1.Comp1 = i.Comp1
			inner join #Input1 i2 on i2.SetID not in (i.SetID, i1.SetID)
								and i2.Comp1 = i.Comp2
								and i2.Comp2 = i1.Comp2
	)
	, g1 as
	(select distinct string_agg([value], ',') within group(order by [value]) Grp
		from g
			cross apply string_split(grp, ',')
		group by ID
	)
select count(*) Answer1
from g1
where exists (select *
				from string_split(grp, ',')
				where [value] like 't%'
			)

;with i as
	(select Comp1, string_agg(Comp2, ',') Connected
		from #Input1
		group by Comp1
	)
	, r as
	(select i.Comp1, ReGroup, cnt
		from i
			inner join i i1 on ',' + i.Connected + ',' like '%,' + i1.Comp1 + ',%'
			cross apply (select string_agg([value], ',') within group (order by [value]) ReGroup, count(*) cnt
							from (select [value]
									from string_split(i1.Connected + ',' + i1.Comp1, ',')
									intersect
									select [value]
									from string_split(i.Connected + ',' + i.Comp1, ',')
								) s
						) r
		group by i.Comp1, ReGroup, cnt
	)
select top 1 ReGroup Answer2
from r
group by ReGroup, cnt
having cnt = count(*)
order by cnt desc