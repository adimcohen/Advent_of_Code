into #Objects
from i1

;with Fits as
	(select k.Obj K, l.Obj L
		from #Objects k
			inner join #Objects l on l.IsKey = 0
									and l.c = k.c
									and l.Height + k.Height <= k.MaxR - 2
		where k.IsKey = 1
		group by k.Obj, l.Obj, k.MaxC
		having count(*) = k.MaxC
	)
