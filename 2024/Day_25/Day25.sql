where Val = '#'

;with i as
	(select *
			, max(r) over(partition by Obj, c) ObjCMaxR
			, min(r) over(partition by Obj, c) ObjCMinR
		from #Input
	)
	, i1 as
	(select distinct Obj, c, ObjCMinR, ObjCMaxR, MaxR, MaxC, iif(max(ObjCMaxR) over(partition by Obj) = MaxR, 1, 0) IsKey
		from i
	)
