;with i as
	(select o.ordinal Obj, rw.ordinal r, co.[value] c, substring(rw.[value], co.[value], 1) Val, max(rw.ordinal) over() MaxR, max(co.[value]) over() MaxC
		from string_split(replace(@Input, concat(char(13), char(10), char(13), char(10)), char(9)), char(9), 1) o
			cross apply string_split(replace(o.[value], char(10), ''), char(13), 1) rw
			cross apply generate_series(cast(1 as int), cast(len(rw.[value]) as int), cast(1 as int)) co
	)
